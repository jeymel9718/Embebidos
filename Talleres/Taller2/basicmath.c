#include <stdio.h>
#include <math.h>

int add(int a,int b){
    return a+b;
}

int sub(int a,int b){
    return a-b;
}

int mul(int a,int b){
    return a*b;
}

int div(int a,int b){
    int result = a/b;
    return result;
}

double root(double a){
    double result = sqrt(a); 
    return result;
}